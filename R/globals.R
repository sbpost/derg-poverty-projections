get_globals <- function() {

	globals <- list()


	# Colors
	globals$ku_red <- "#901A1E"
	globals$ku_gray <- "#666666"
	globals$cond_true <- "#2c7fb8"
	globals$early_color <- "#2b83ba"
	globals$recent_color <- globals$ku_red
	globals$colors_4 <- c(globals$ku_red, "#fdae61", "#abd9e9", "#2c7bb6")
	
	return(globals)
}
